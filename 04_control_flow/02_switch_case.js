// switch (key) {
//     case value:
        
//         break;

//     default:
//         break;
// }

const month = 4 //we can also take string as value 

switch (month) {
    case 1:
        console.log('jan');
        break;
    case 2:
        console.log('feb');
        break;
    case 3:
        console.log('mar');
        break;
    case 4:
        console.log('april');
        break;

    default:
        console.log('enter correct value of a month');
        break;
}